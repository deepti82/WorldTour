import UIKit

class NotificationFooter: UIView {

    @IBOutlet weak var NFReadMark: UIImageView!
    
    override init(frame: CGRect) {
        super.init(frame: frame)
        loadViewFromNib ()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
    }
    
    func loadViewFromNib() {
        let bundle = Bundle(for: type(of: self))
        let nib = UINib(nibName: "notificationFooterView", bundle: bundle)
        let view = nib.instantiate(withOwner: self, options: nil)[0] as! UIView
        view.frame = bounds
        view.autoresizingMask = [.flexibleWidth, .flexibleHeight]
        self.addSubview(view);
    }
    
    func updateReadStatus(read: String) {
        if read == "unread" {
            NFReadMark.tintColor = UIColor.white
        }
        else {
            NFReadMark.tintColor = UIColor.red
        }
    }
}
